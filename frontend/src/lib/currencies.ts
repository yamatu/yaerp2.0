export function getCurrencyCodes() {
  const supportedValuesOf = (Intl as unknown as { supportedValuesOf?: (key: string) => string[] }).supportedValuesOf
  if (supportedValuesOf) {
    try {
      return supportedValuesOf('currency').sort()
    } catch {
      // Older engines use the maintained ISO fallback below.
    }
  }
  return FALLBACK_CURRENCIES
}

export function getCurrencyLabel(code: string) {
  try {
    const DisplayNames = (Intl as unknown as { DisplayNames?: typeof Intl.DisplayNames }).DisplayNames
    if (DisplayNames) {
      const name = new DisplayNames(['zh-CN'], { type: 'currency' }).of(code)
      if (name && name !== code) return `${code} · ${name}`
    }
  } catch {
    // Code-only labels remain searchable and unambiguous.
  }
  return code
}
